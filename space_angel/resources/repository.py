    # initialization
    # -----------------------------------------------------
    def __init__(self):
        self.__repo = Repo()

    # -----------------------------------------------------
    # checkout
    # -----------------------------------------------------
    def checkout(self, name, point = None):
        if point:
            self.__repo.git.checkout(point, b=name) 
        else:
            self.__repo.git.checkout(name)
    
