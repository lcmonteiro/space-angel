# -*- coding: utf-8 -*-
# -------------------------------------------------------------------------------------------------
# Terminal - context
# -------------------------------------------------------------------------------------------------
class Terminal:
    # -------------------------------------------------------------------------
    # Command object
    # -------------------------------------------------------------------------
    class Command(object):
        # -------------------------------------------------
        # initialization
        # -------------------------------------------------
        def __init__(self, shell, cmd):
            pass

        # -------------------------------------------------
        # execute
        # -------------------------------------------------
        def __call__(self):
            pass
    # -------------------------------------------------------------------------
    # Shell object
    # -------------------------------------------------------------------------
    class Shell(object):
        # -------------------------------------------------
        # initialization
        # -------------------------------------------------
        def __init__(self):
            pass

    # -------------------------------------------------------------------------
    # initialization
    # -------------------------------------------------------------------------
    def __init__(self, commands):
        # create shell
        self.__shell = Terminal.Shell()
        # create commands
        for k, c in commands.items():
            setattr(self, k, Terminal.Command(self.__shell, c))

# -------------------------------------------------------------------------------------------------
# end
